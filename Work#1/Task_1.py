# Задача студентов состоит в расчете среднеквадратичной ошибки (MSE) и средней абсолютной ошибки (MAE), для данного набора данных без использования модели. 
# Студенты должны использовать правильные значения фактической переменной и предсказанные значения, для расчета функций потерь. 
# Они также должны проанализировать результаты и объяснить, какие значения функции потерь указывают на лучшую точность предсказания.


import numpy as np

# Фактические и предсказанные значения
actual_values = np.array([100, 150, 200, 250])
predicted_values = np.array([90, 140, 180, 270])

# Вычисление MSE
mse = np.mean((actual_values - predicted_values) ** 2)

# Вычисление MAE
mae = np.mean(np.abs(actual_values - predicted_values))

# Вывод результатов
print(f"Среднеквадратичная ошибка (MSE): {mse}")
print(f"Средняя абсолютная ошибка (MAE): {mae}")


# Среднеквадратичная ошибка (MSE):
# Значение MSE = 250 указывает на средний квадрат отклонений предсказанных значений от фактических. Чем меньше MSE, тем точнее модель.
# MSE чувствительна к большим отклонениям (ошибкам). Например, если предсказания сильно отклоняются от фактических значений, MSE будет высоким.

# Средняя абсолютная ошибка (MAE):
# MAE = 15 указывает на среднюю абсолютную ошибку между фактическими и предсказанными значениями. 
# Это более "интуитивный" показатель, поскольку измеряет ошибки в тех же единицах, что и сами данные.
# MAE менее чувствительна к большим отклонениям, чем MSE, так как она использует абсолютные значения ошибок, не возводя их в квадрат.

# В данном случае, MSE = 250 и MAE = 15. 
# Меньшее значение MAE указывает на то, что предсказания относительно фактических значений более стабильны, но MSE помогает подчеркнуть, 
# что ошибка в 20 (в случае 200 и 180 или 250 и 270) оказывает большее влияние на общую ошибку, чем ошибки, равные 10.